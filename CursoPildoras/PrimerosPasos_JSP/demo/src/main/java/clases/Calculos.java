package clases;

public class Calculos {
    

    public static int metodoSuma(int num1, int num2) {
        int resultado;
        resultado = num1 + num2;
        return resultado;
    }

    public static int metodoResta(int num1, int num2) {
        int resultado;
        resultado = num1 - num2;
        return resultado;
    }

    public static int metodoMultiplica(int num1, int num2) {
        int resultado;
        resultado = num1 * num2;
        return resultado;
    }
}
